### 연산자 연습 프로그램(학번, 이름)
num1 = int(input("data1=>"))
num2 = int(input("data2=>"))
print("%d + %d = %10d" %(num1, num2, num1+num2))
print("%d ** %d = %10d" %(num1, num2, num1**num2))
print("%d // %d = %10d" %(num1, num2, num1//num2))
print("%d > %d = %d" %(num1, num2, num1>num2))
print(num1 >=10 and num2 !=100)